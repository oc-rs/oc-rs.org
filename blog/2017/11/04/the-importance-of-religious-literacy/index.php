            <article>
                <p>Religion impacts many aspects of our lives, and religious events are all around us, both positive and negative. Over the last few years, the idea of improving religious literacy has been gaining ground, with the launch of the <a target="_BLANK" href="https://rlp.hds.harvard.edu/home">Religious Literacy Project</a> at the Harvard Divinity School in 2015, and the publication of the All-Party Parliamentary Group on Religious Education’s report (<a target="_BLANK" href="http://www.reonline.org.uk/wp-content/uploads/2016/07/APPG-on-RE-Improving-Religious-Literacy-full-report.pdf">Improving Religious Literacy</a>) in 2016.</span></p>

                <p>But what is religious literacy, and how is it different to religious education, comparative religion, or religious studies?</p>

                <img style="width: 100%; padding-bottom: 10px" src="/img/jesus-statue.jpeg">
                
                <p>Fiona Bruce's introduction to the APPG report makes it clear that religious literacy is not intended to be a vehicle for academic knowledge of core beliefs or essential practices - a sentiment echoed by Adam Dinham, author of Religious Literacy in Policy and Practice. He states in an interview with On Religion:</p>
                
                <blockquote>‘For me, it is not the ability to answer questions like ‘what are the Ten Commandments’ or ‘what language is the Qu’ran written in?’. It’s not a miraculous knowledge of more religious traditions and theologies than any one person could possibly hold in their heads’ (<a target="_BLANK" href="http://www.onreligion.co.uk/what-is-religious-literacy-qa-with-adam-dinham/">On Religion, 2016</a>).</blockquote>

                <p>Rather, as he goes on to elaborate <a target="_BLANK" href="https://www.sheffield.ac.uk/news/nr/comment-religious-literacy-what-is-the-future-for-religion-and-belief-1.570731">elsewhere</a>, religious literacy is a skillset, with three core elements:</p>
                    <ul>
                        <li>Understanding religion as a sociological phenomenon, not a monolithic entity</li>
                        <li>Understanding the emotional and deeply personal nature of these behaviours and beliefs</li>
                        <li>Building the knowledge we need, that is relevant to our area of interest</li>
                    </ul>
                <p>In other words, religious literacy means appreciating that religions are not neat boxes, they are filled with internal diversity, complex histories, and competing ideologies. This is the first skill - avoiding the trap of saying ‘Christians believe….’ or ‘Islam teaches…’.</p>

                <p>The second skill means appreciating the personal disposition of adherents when talking about their traditions. We may find that though Roman Catholics believe a particular catechism, some do not feel very strongly about it, and it does not drive their daily life. The reverse is also possible, so care must be taken before drawing unfounded conclusions.</p>

                <p>The final skill, based on the first two, is to look for information that is relevant to the people you are talking about. It might not matter what South Indian Hindus believe about Śiva if the religious adherents you are speaking to are Hindus from North India. Then again, it might be very important - you need the first two skills to find out.</p>

                <p>At its core, religious literacy is a skillset that allows you to engage with religious events and individuals intelligently, without falling into some of the potholes that frequently cause difficulties in understanding. It encourages a mindset that is open and sympathetic to religiosity without being apologetic or doctrinal, and resists lumping people together - rather it looks first to the individual and the community to understand how they experience and express <strong>their</strong> religion.</p>

                <p>It does not require you to say this or that religion is true, nor does it mean you cannot be critical or ask difficult questions. However, it does mean you are less likely to be misled by sensationalism and bigotry, and more inclined to hear both sides of a debate. It is important to be able to contextualise world events and narratives, and to appreciate the symbolism and significance behind otherwise obscure rituals, values, or practices.</p>

                <p>Dinham suggests that people have neglected to talk about religion in an open and constructive way, and this has allowed these skills to atrophy; in place of intelligent understanding, ignorance and bigotry have become acceptable (or at least commonplace). It seems clear that religion is not going away, and if we hope to build a sustainable cultural landscape, we must find our own way to meaningfully engage with it.</p>

                <p>The APPG report summarises the subject by stating: '...improving religious literacy means equipping people with the knowledge and skills to understand and discuss religions and issues around them confidently, accurately and critically.'</p>

                <p>Religious literacy is a personal - and interpersonal - skill that facilitates a balanced and intelligent engagement with the religious aspects of human culture. It accepts diversity, resists simple labels, and seeks to contextualise and understand people in their own terms. As the world gets ever smaller, and cultures ever more intertwined, this skill can only become more important and relevant. Without it, the potential for misunderstanding, hatred, and even violence, can grow.</p>

