                <img style="width: 100%" src="/img/hindu-sculpture.jpg"/>
                <p>There are, broadly speaking, two ways to approach religion as an academic subject.</p>

                <p>The first is based on a sort of family tree model; it looks a bit like this.</p>

                <blockquote>
                    <pre>Religion
--Christianity
----Catholicism
----Eastern Orthodoxy
------Greek Orthodox Church
------Russian Orthodox Church
----Protestantism
------Methodism
------Evangelism</pre>
                </blockquote>

                <p>And so on.</p>

                <p>It's useful from a historical perspective, because we can easily trace back along a tradition's lineage to see where it diverged from its closest neighbour.</p>

                <p>But if we collapse the hierarchy, we lose all that distinction. Methodists and Evangelicals are <em>just Protestants</em>. Protestants and Catholics are <em>just Christians</em>. Christians and Muslims are <em>just religious people.</em></p>

                <p>This collapse of the hierarchies happens often in the teaching of religion because there are <strong>a lot</strong> of different sub-traditions. Hundreds, if not thousands. It's just not feasible to learn all of them.</p>

                <p>So, people learn what Christians <em>generally</em> believe and <em>usually</em> practice. The same goes for Muslims, Hindus, and any other 'top level' religious tradition (top level only in the sense of this family tree model).</p>

                <p>There's a danger here - so frequently encountered that it's got its own name: the <strong>world religions paradigm</strong>.</p>

                <p>The world religions paradigm is a way of thinking about religion that tends to look at the largest religious groups (the 'world' religions), and not only ignores smaller, or more localised traditions, but glosses over the internal diversity within these larger groups.</p>

                <p>The family tree model is useful, but not entirely accurate.</p>

                <p>There is an alternative approach.</p>

                <p>If we look more closely, we can see that each religious group, sub-group, and individual, <strong>shares</strong> certain characteristics even if they're not directly related. So, for instance, we might notice that worship of a deity is a common theme for many religious traditions (but not all). We could then start to rebuild the family tree based not on historical lineage, but on practical and conceptual convergences.</p>

                <p>In other words, wouldn't it make sense to group the worship with the worship, the salvation with the salvation, and the transcendance with the transcendance?</p>

                <p>Then we can start to build an image of the sorts of beliefs and behaviours that are common to religion as a whole, illustrated by examples drawn from across the world.</p>

                <p>Rather than giving a laundry list of what religion X believes, we want to bring together Christian prayers and Sikh prayers, Islamic offerings and Hindu offerings, Jain rituals and Zoroastrian rituals, and see how much similarity runs through them.</p>

                <p>Then we want to stand religious neighbours together and show how much diversity separates them. Why aren't Protestants and Catholics the same? Where is the split between Sunni and Ahmadiyya Muslim communities? How can Vaishnavite Hindus worship Vishnu as the supreme deity while Shaivite Hindus praise Shiva as the ultimate?</p>

                <p>Whilst it's important to preserve an accurate sense of religious traditions as they exist in the popular consciousness (i.e. the world religions paradigm), we would recommend this alternative, thematic approach for the academic study of religion - and this is the approach that the OCRS will endeavour to support in its own teaching.</p>

                <p>Each 'instance of religiousness' (a term that avoids the dichotomy of internal experience and external expression) represents both a functional purpose (community cohesion, cosmology, etc.) and a substantive experience (personal salvation, communion with God, etc.), situated within a cultural context.</p>

                <p>The aim of this approach is to present 'instances of religiousness' as situated within a network of related instances, based on key attributes drawn from functional and substantive assessments, and including insider and outsider perspectives.</p>

                <p>We must be careful, nevertheless, to ensure that we don't try to make these instances equivalent - they are still very much unique, and will still have many interesting and substantial divergences between each other.</p>

                <p>To present a linguistic analogy, we're interested in the differing expressions of a feeling or object in languages across the world. Rather than providing a prescriptive description of 'how French people speak' and 'how Chinese people speak', we would consider the human tendency to greet people, with case studies drawn from various cultures and contexts - for instance bonjour and ni hao could be examined as examples.</p>

                <p>It's still important to understand the context, since otherwise rich and complex traditions could be flattened and religious behaviour would be abstracted to serve the purposes of illustrating preconceived categories. The categories should emerge from an investigation of religious behaviour and experience, not the other way around.</p>

                <p>We want to upend the idea that a person's religion is simple, contained, and static. Instead we want to shed light on the dramatic dynamism that exists in people's attempts to find meaning and purpose within the universe, and how the techniques, narratives, and ideas that they develop can bring people together and tear them apart.</p>

                <p>In short, we want to show how really, REALLY interesting religion is.</p>

