            <h2>The details</h2>
            <p>See below for more detailed information about who we are and how we operate.</p>
            <h3 id="mission-statement">Mission statement</h3>
            <p>The OCRS is dedicated to promoting and facilitating the academic study of religion for a wide and diverse audience.</p>
            <h3 id="aims-methods">Aims &amp; methods</h3>
            <p>Using a variety of modern, online delivery methods, the OCRS aims to provide information about religious studies in a clear and accessible format - wherever possible writing in a simple, conversational style.</p>
            <p>The OCRS will package information in different ways, including free PDF documents, links to third parties, and paid online courses.</p>
            <h3 id="audience">Audience</h3>
            <p>The OCRS is aimed at adults who are interested in learning what religion is, how different religions believe and behave, and how to study religion in an academic style.</p>
            <p>The OCRS aims to fill a middle ground between accessibility and reliability: anyone can read a wikipedia article, but equally anyone can write a wikipedia article; likewise, universities offer authentic knowledge, but often hidden behind challenging technical language and high financial barriers.</p>
            <p>At present, the OCRS is only available in English - however translation into other languages could be made available in the future, depending on growth and interest.</p>
            <h3 id="world-religions">World religions</h3>
            <p>The OCRS regards the world religions paradigm as a potential source of misunderstanding, capable of reinforcing politicised stereotypes, although we recognise that it serves a useful introductory purpose.</p>
            <p>Where possible, the OCRS aims to subvert the expectations of a traditional division of religion into the &#39;big five&#39;, and encourages thematic approaches that explore religious practice in a more realistic way, grounded in lived experience, and presented as part of a broad cultural context.</p>
